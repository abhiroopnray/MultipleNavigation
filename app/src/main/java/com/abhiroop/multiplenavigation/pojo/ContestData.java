package com.abhiroop.multiplenavigation.pojo;

public class ContestData {

    private int winningAmount;
    private int entryFee;
    private int totalParticipants;
    private int joinedParticipants;

    public int getWinningAmount() {
        return winningAmount;
    }

    public void setWinningAmount(int winningAmount) {
        this.winningAmount = winningAmount;
    }

    public int getEntryFee() {
        return entryFee;
    }

    public void setEntryFee(int entryFee) {
        this.entryFee = entryFee;
    }

    public int getTotalParticipants() {
        return totalParticipants;
    }

    public void setTotalParticipants(int totalParticipants) {
        this.totalParticipants = totalParticipants;
    }

    public int getJoinedParticipants() {
        return joinedParticipants;
    }

    public void setJoinedParticipants(int joinedParticipants) {
        this.joinedParticipants = joinedParticipants;
    }
}
